class Solution(object):
    def multiply(self, num1, num2):
        """
        :type num1: str
        :type num2: str
        :rtype: str
        """
        conv1 = int(num1)
        conv2 = int(num2)
        
        mul = conv1 * conv2;
        
        strings = str(mul)
        
        return strings
        